package com.example.focusgroupheroican.clases;


public class ClsComment {

	public String usu_email, usu_nombre, usu_edad, usu_cargo, usu_genero;
	public String answer, comment;
	
	public ClsComment(String usu_email, String usu_nombre, String usu_edad,
			String usu_cargo, String usu_genero, String answer, String comment) {
		this.usu_email = usu_email;
		this.usu_nombre = usu_nombre;
		this.usu_edad = usu_edad;
		this.usu_cargo = usu_cargo;
		this.usu_genero = usu_genero;
		this.answer = answer;
		this.comment = comment;
	}
	
}
